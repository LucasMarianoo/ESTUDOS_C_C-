#include <stdio.h>
#include <stdlib.h>

/*
1. Faça um programa que exiba os números de 1 a 10. Utilize a estrutura do-while para realizar a repetição.
*/

int main() {

    system("cls");

    int num = 1;

    while (num <= 10) {
        printf("%d \n", num);
        num++;
    }

    return 0;
}
